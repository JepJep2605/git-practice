let firstFavNumb = 7; 
let secondFavNumb = 3;   

// Greater than
console.log(firstFavNumb > secondFavNumb); 

// Less than
console.log(firstFavNumb < secondFavNumb); 

// Greater than or equal to
console.log(firstFavNumb >= secondFavNumb); 

// Less than or equal to
console.log(firstFavNumb <= secondFavNumb);

// Strict equality (===)
console.log(firstFavNumb === secondFavNumb); 

// Loose equality (==)
console.log(firstFavNumb == secondFavNumb); 

// Strict inequality (!==)
console.log(firstFavNumb !== secondFavNumb);

// Loose inequality (!=)
console.log(firstFavNumb != secondFavNumb); 

// AND (both conditions must be true)
console.log(firstFavNumb > 0 && secondFavNumb > 0);

// OR (one condition must be true)
console.log(firstFavNumb > 10 || secondFavNumb > 0); 

// NOT (negation)
console.log(!(firstFavNumb > secondFavNumb));